/* USER CODE BEGIN 4 */







void run_while()
{
	uint8_t count_sample=SAMPLE,index=0;
//	mean_
	/* data_im array before fft calculation is always =0*/
	while(1)
	{
		if(__HAL_TIM_GET_COUNTER(&htim2)>INTERVAL_SAMPLE)
		{
			if (index==count_sample )
			{
				break;
			}

			/*read SAMPLE/2 values (ADC) and store to data_re_fisrt  */
			__HAL_TIM_SET_COUNTER(&htim2,0);
			HAL_ADC_Start_DMA(&hadc1,&adc_val,1);
			const float map_adc_val=(float)map(adc_val,0,4095,MIN_AMP,MAX_AMP);

//			mean_amp_second+= abs(map_adc_val);
			data_re_second[index]=map_adc_val;
			data_im_second[index]=0;
			if (abs(map_adc_val)>AMP_THRESHOLD)
			{
				flag_loud_second=1;
			}

			if (index<HALF_SAMPLE)
			{
				/*store adc_val in second half data_re_first */

//				mean_amp_first+=abs(map_adc_val);
				data_re_first[HALF_SAMPLE+index]=map_adc_val;
				data_im_first[HALF_SAMPLE+index]=0;
				if (abs(map_adc_val)>AMP_THRESHOLD)
				{
					flag_loud_first =1;
				}

			}
			if (index>=HALF_SAMPLE)
			{
				if (abs(map_adc_val)>AMP_THRESHOLD)
				{
					flag_loud_first=1;
				}

				/*store adc_val in first half data_re_first */
//				mean_amp_first+= abs(map_adc_val);
				data_re_first[index-HALF_SAMPLE]=map_adc_val;
				data_im_first[index-HALF_SAMPLE]=0;
			}
			if (index==SAMPLE/2)
			{
				if (flag_loud_first==1)
				{
					fft(data_re_first,data_im_first,SAMPLE);
					const uint8_t argmax_first=argmax_fft(data_re_first,data_im_first,SAMPLE);
//					const uint32_t value= sqrt(pow(data_re_second[argmax_first],2)+pow(data_im_second[argmax_first],2));
//					printf("1          %d                  %lu\n ",argmax_first,value);
					if (argmax_first==FREQ_INDEX)
					{
						if (__HAL_TIM_GET_COUNTER(&htim4)>500)
						{
							/*avoid led on off continuously, delay 50 ms, prescaler of tim4=10000-1 */
							__HAL_TIM_SET_COUNTER(&htim4,0);
							HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
//							printf("1      clap detect   %f\n",mean_amp_first/SAMPLE );
						}

//

					}

				}
//				mean_amp_first=0;
				flag_loud_first=0;


			}



			index++;
		}
	}
	/*index =count_sample */
	if (flag_loud_second==1)
	{
		fft(data_re_second,data_im_second,SAMPLE);
		const uint8_t argmax_second=argmax_fft(data_re_second,data_im_second,SAMPLE);
//		const uint32_t value= sqrt(pow(data_re_second[argmax_second],2)+pow(data_im_second[argmax_second],2));
//		printf("1          %d               %lu\n",argmax_second, value);
		if (argmax_second==FREQ_INDEX)
		{
			if (__HAL_TIM_GET_COUNTER(&htim4)>500)
			{
				/*avoid led on off continuously, delay 100 ms, prescaler of tim4=10000-1  */
				__HAL_TIM_SET_COUNTER(&htim4,0);
				HAL_GPIO_TogglePin(GPIOC, GPIO_PIN_13);
//							printf("2      clap detect      %f\n",mean_amp_second/SAMPLE);
			}


		}
	}
//	mean_amp_second=0;
	flag_loud_second=0;
}



